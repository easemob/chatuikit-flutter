/// Chat UI Kit Action Events
enum ChatUIKitEvent {
  /// Group id copied
  groupIdCopied,

  /// User id copied
  userIdCopied,

  /// Message copied
  messageCopied,

  /// Voice type not supported
  voiceTypeNotSupported,

  /// No record permission
  noRecordPermission,

  /// No camera permission
  noCameraPermission,

  /// No storage permission
  noStoragePermission,

  /// Message downloading
  messageDownloading,
}
